	<h2>Leitura de um pneu:</h2>
    <p>A lateral do pneu, chamada de costado, possui uma série de códigos e inscrições, tais como:</p>
        
    <p>Exemplo: 175/70R 13</p>
    
    <img class="help_1" src=<?= base_url("img/help/pneu_medida.png")?> alt="dica leitura medidas pneu">   
	
    	<div class="row-help">
            175- Indica a largura nominal da secção do pneu, em milímetros.
        </div>
            
        <div class="row-help">
            70- Indica a série do perfil do pneu, isto é; a relação percentual entre a altura e a largura da secção do pneu. Quanto menor for esse número mais baixo será o perfil do pneu, ou seja, um pneu da série 60 (195/60R14) tem um perfil mais baixo e uma aparência mais larga do que um pneu da série 70 e, assim sucessivamente.
        </div>
               
        <div class="row-help">
            R- Indica que o pneu é de construção radial (no tipo de construção radial, os cordonéis da lona se estendem de talão a talão, no sentido radial, formando um ângulo reto em relação à linha central da banda de rodagem. 
        </div>
                
        <div class="row-help">
            13- Indica o diâmetro interno do pneu, em polegadas (= diâmetro do aro)
        </div>		
	
